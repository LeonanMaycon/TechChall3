// Utilitários para acessar o tema
export const getColor = (colorPath) => {
  const keys = colorPath.split('.');
  return keys.reduce((obj, key) => obj?.[key], theme.colors);
};

export const getSpacing = (size) => theme.spacing[size] || size;

export const getBreakpoint = (breakpoint) => theme.breakpoints[breakpoint];

export const getFontSize = (size) => theme.typography.fontSize[size];

export const getFontWeight = (weight) => theme.typography.fontWeight[weight];

